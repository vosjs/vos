/**
 * Generates the injectable elements bundle: a self-contained IIFE that defines
 * the global `__vosElementsFactory`, exported as a string constant so both the
 * web app (Vite) and the API (Cloudflare Workers) can inline it into the render
 * template via generateRenderTemplate({ elementsBundleCode }).
 *
 * THREE is provided at runtime (passed to createVosElements), so it's external.
 * Runs after tsup in the `build` script (tsup's `clean` already created dist/).
 */
import { buildSync } from 'esbuild'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const distDir = resolve(__dirname, 'dist')

const { outputFiles } = buildSync({
  entryPoints: [resolve(__dirname, 'src/index.ts')],
  bundle: true,
  format: 'iife',
  globalName: '__vosElementsFactory',
  target: 'es2020',
  external: ['three'],
  write: false,
})

const iifeCode = outputFiles[0].text

mkdirSync(distDir, { recursive: true })
writeFileSync(
  resolve(distDir, 'bundle.js'),
  `// AUTO-GENERATED by bundle.mjs — injectable IIFE defining globalThis.__vosElementsFactory\nexport const elementsBundleCode = ${JSON.stringify(iifeCode)}\n`,
)
writeFileSync(
  resolve(distDir, 'bundle.d.ts'),
  `export declare const elementsBundleCode: string\n`,
)

console.log('Built elements bundle -> dist/bundle.js')
