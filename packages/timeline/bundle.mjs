/**
 * Generates the injectable timeline runtime: a self-contained IIFE exposing
 * `globalThis.__vosTimeline` ({ sample, mapTime, totalDuration, ... }),
 * exported as a string constant (`timelineRuntimeCode`). App lowerings inline
 * it into their constant program (typically the `setup` function-string) so
 * keyframes/segments in `ctx.data` evaluate with EXACTLY the code the host
 * used — one implementation on both sides of the bridge.
 *
 * The explicit globalThis assignment matters: esbuild's `globalName` declares a
 * `var`, which would be scoped to the enclosing function when the IIFE is
 * inlined inside a function-string rather than at the top level of a script.
 */
import { buildSync } from 'esbuild'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const distDir = resolve(__dirname, 'dist')

const { outputFiles } = buildSync({
  entryPoints: [resolve(__dirname, 'src/runtime.ts')],
  bundle: true,
  format: 'iife',
  globalName: '__vosTimeline',
  target: 'es2020',
  minify: true,
  write: false,
})

const iifeCode = `${outputFiles[0].text};globalThis.__vosTimeline = __vosTimeline;`

mkdirSync(distDir, { recursive: true })
writeFileSync(
  resolve(distDir, 'bundle.js'),
  `// AUTO-GENERATED by bundle.mjs — injectable IIFE defining globalThis.__vosTimeline\nexport const timelineRuntimeCode = ${JSON.stringify(iifeCode)}\n`,
)
writeFileSync(
  resolve(distDir, 'bundle.d.ts'),
  `export declare const timelineRuntimeCode: string\n`,
)

console.log('Built timeline runtime bundle -> dist/bundle.js')
