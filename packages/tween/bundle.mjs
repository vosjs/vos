/**
 * Generates the injectable tween runtime: a self-contained IIFE exposing
 * `globalThis.__vosTween` ({ createTweenRecorder, extractTimeline, ... }),
 * exported as a string constant (`tweenRuntimeCode`). The render template
 * inlines it when the host selects the vos tween backend, so `deps.gsap` can
 * be a recorder facade with no GSAP import — EXACTLY the code the host's
 * parity harness verified, on both sides of the bridge.
 *
 * The explicit globalThis assignment matters: esbuild's `globalName` declares a
 * `var`, which would be scoped to the enclosing function when the IIFE is
 * inlined inside a function body rather than at the top level of a script.
 */
import { buildSync } from 'esbuild'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const distDir = resolve(__dirname, 'dist')

const { outputFiles } = buildSync({
  entryPoints: [resolve(__dirname, 'src/runtime.ts')],
  bundle: true,
  format: 'iife',
  globalName: '__vosTween',
  target: 'es2020',
  minify: true,
  write: false,
})

const iifeCode = `${outputFiles[0].text};globalThis.__vosTween = __vosTween;`

mkdirSync(distDir, { recursive: true })
writeFileSync(
  resolve(distDir, 'bundle.js'),
  `// AUTO-GENERATED by bundle.mjs — injectable IIFE defining globalThis.__vosTween\nexport const tweenRuntimeCode = ${JSON.stringify(iifeCode)}\n`,
)
writeFileSync(
  resolve(distDir, 'bundle.d.ts'),
  `export declare const tweenRuntimeCode: string\n`,
)

console.log('Built tween runtime bundle -> dist/bundle.js')
